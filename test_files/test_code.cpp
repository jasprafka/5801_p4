#include <iostream>

// Using namespace std is bad practice
using namespace std;

int main() {
    // Variable declarations
    int x = 0;
    int y = 0;
    int z = 0;

    // Variables must be declared before they are used
    cout << "x = " << x << endl;
    cout << "y = " << x << endl;
    cout << "z = " << x << endl;

    return 0;
}